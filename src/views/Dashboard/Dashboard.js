class Dashboard extends Component {
  constructor(props) {
    super(props);

    this.toggle = this.toggle.bind(this);
    this.onRadioBtnClick = this.onRadioBtnClick.bind(this);

    this.state = {
      dropdownOpen: false,
      radioSelected: 2,
      isLogedIn: true,
      questions: false
    };
  }

  handleQuestionButton = () => {
    this.state.questions ? this.setState({ questions: false }) : this.setState({ questions: true });
  }

  toggle() {
    this.setState({
      dropdownOpen: !this.state.dropdownOpen,
    });
  }

  onRadioBtnClick(radioSelected) {
    this.setState({
      radioSelected: radioSelected,
    });
  }

  loading = () => <div className="animated fadeIn pt-1 text-center">Loading...</div>

  render() {
    return (
      <div className="animated fadeIn">
        {
          this.state.questions ?
            <Button onClick={this.handleQuestionButton}>Hide-Bot</Button> :
            <Button onClick={this.handleQuestionButton}>Angel-Bot</Button>
        }
        {this.state.questions ? <LexBot /> : ""}
      </div>
    )
  }
}

export default Dashboard; 